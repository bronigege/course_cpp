#ifndef FUNCIONES_CALCULADORA_H
#define FUNCIONES_CALCULADORA_H



class Calculadora {
public:

    auto sumar(auto x, auto y) {
        return x + y;
    }
    auto restar(auto x, auto y) {
        return x - y;
    }
    auto dividir(auto x, auto y) {
        return x / y;
    }
    auto multiplicar(auto x, auto y) {
        return x + y;
    }
};





#endif //FUNCIONES_CALCULADORA_H
